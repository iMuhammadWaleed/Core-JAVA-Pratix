public class LeftHalfPyramid
{
    public static void main(String[] args) {
        System.out.println("         *");
        System.out.println("       * *");
        System.out.println("     * * *");
        System.out.println("   * * * *");
        System.out.println(" * * * * *");

        //Using Escape Sequence
        System.out.println("\n");

        System.out.println("         *\n       * *\n     * * *\n   * * * *\n * * * * *");

    }
}
